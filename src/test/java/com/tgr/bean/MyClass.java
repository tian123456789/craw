package com.tgr.bean;

public class MyClass {

	private String name;
	
	private int age;
	
	public MyClass() {
	}

	public MyClass(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	
}
